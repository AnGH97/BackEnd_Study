package variable;

public class IntTest01 {

	public static void main(String[] args) {
		//정수끼리 연산에서는 정수값이 나온다.
		
		int a = 5, b = 3;		//같은 자료형은 연달아서 변수 선언이 가능하다.
								//같은 변수명은 한번만 선언이 가능하다.
		
		//산술연산자
		System.out.println(a + b);	//더하기
		System.out.println(a - b);	//빼기
		System.out.println(a * b);	//곱하기
		System.out.println(a / b);	//나누기 - 정수끼리 연산은 몫만 구해진다.
		System.out.println(a % b);	//나머지 ***
		
	}

}
